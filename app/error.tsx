"use client";

import { useEffect } from "react";

export default function Error({
  error,
  reset,
}: {
  error: Error & { digest?: string };
  reset: () => void;
}) {
  useEffect(() => {
    console.error(error);
  }, [error]);

  return (
    <div className="flex min-h-[50vh] flex-col items-center justify-center gap-4 px-4 text-center">
      <h2 className="text-2xl font-bold text-neutral-900 dark:text-white">Something went wrong!</h2>
      <p className="text-neutral-500 dark:text-neutral-400">
        We couldn't load the stories. Please try again later.
      </p>
      <button
        onClick={() => reset()}
        className="rounded-md bg-orange-500 px-4 py-2 text-sm font-medium text-white transition-colors hover:bg-orange-600"
      >
        Try again
      </button>
    </div>
  );
}
